    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:
        Usuario objUsu = new Usuario();

        String indexComboBox = (String) cbTipoUser.getSelectedItem();
        while (txtNomeLogin.getText().equals("") || txtSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            break;
        }
        while (indexComboBox.equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione um tipo de Usuario");
            break;
        }

        objUsu.setNome_Usu(txtNomeLogin.getText());
        objUsu.setSenha_Usu(txtSenha.getText());
        objUsu.setTipo_Usu((String) cbTipoUser.getSelectedItem());
        
        UsuarioDAO objUsuDAO = new UsuarioDAO();
        ResultSet rsUsuDAO = objUsuDAO.autenticacaoUsuario(objUsu);
        
        try {
            if (rsUsuDAO.next()) {
                String dadoCbTipo = objUsu.getTipo_Usu();
                if (dadoCbTipo == "Admin") {
                    TelaInvAdm telaAdm = new TelaInvAdm();
                    telaAdm.setVisible(true);
                    this.dispose();
                } else if (dadoCbTipo == "Usuario") {

                    TelaInvUsu telaUser = new TelaInvUsu();
                    telaUser.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Email ou Senha Incorretos");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro no Login" + ex);
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

