    private void btnAddItensActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItensActionPerformed
        // TODO add your handling code here:
        Itens objItens = new Itens();
        ItensDAO objItensDAO = new ItensDAO();

        String indexComboBox = (String) cbCategoria1.getSelectedItem();

        while (txtNomeItem1.getText().equals("") || txtEspecItem1.getText().equals("") || taDescricaoItem1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            break;
        }
        if (indexComboBox.equals("Selecione")) {
            JOptionPane.showMessageDialog(null, "Selecione uma categoria");
        } else {

            objItens.setNome_Item(txtNomeItem1.getText());
            objItens.setDescricao_Item(taDescricaoItem1.getText());
            objItens.setCategoria_Item(indexComboBox);
            objItens.setEspecificacao_Item(txtEspecItem1.getText());

            objItensDAO.save(objItens);
            JOptionPane.showMessageDialog(null, "Item '" + txtNomeItem1.getText() + "' foi cadastrado com sucesso !");
            attTabelaItens();
        }
        txtNomeItem1.setText("");
        taDescricaoItem1.setText("");
        cbCategoria1.setSelectedIndex(0);
        txtEspecItem1.setText("");
    }//GEN-LAST:event_btnAddItensActionPerformed

