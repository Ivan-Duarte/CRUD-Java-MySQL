    private void btnExcluirUsuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirUsuActionPerformed
        // TODO add your handling code here:
        if (tbUsuarios.getSelectedRow() != -1) {

            int row = tbUsuarios.getSelectedRow();
            String titulo = tbUsuarios.getModel().getValueAt(row, 1).toString();
            int result = JOptionPane.showConfirmDialog(null, "ATENÇÃO ! Deseja excluir o Usuario: " + titulo + " ?");

            if (result == 0) {

                String cell = tbUsuarios.getModel().getValueAt(row, 0).toString();
                String sql = "DELETE FROM usu WHERE id_Usu = " + cell;
                try {
                    PreparedStatement stmt = con.prepareStatement(sql);
                    stmt.execute();
                    JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                    attTabelaItens();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao Excluir" + e);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Item");
        }
        attTabelaUsu();
    }//GEN-LAST:event_btnExcluirUsuActionPerformed

